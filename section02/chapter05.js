// 2. 객체타입
// -> 참조값을 통해 변수에 저장되고 복사된다. 가변값이다. (메모리 값 수정한다.)
// -> 의도치 않게 값이 수정될 수 있다. -> 사이드 이펙트 발생
// -> 얕은 복사(객체의 참조값을 복사하는)와
// -> 깊은 복사(새로운 객체를 생성 후 값만 가져오는)가 있다.
// -> 객체간의 비교는 기본적으로 참조값을 기준으로 이루어진다.
// -> 배열과 함수도 객체이다.
let o1 = { name: "김민혁" };
let o2 = o1;
let o3 = { ...o1 };

console.log(o1 === o2); // true
console.log(o1 === o3); // false
console.log(JSON.stringify(o1) === JSON.stringify(o3)); // true
